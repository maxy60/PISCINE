#include <unistd.h>

void	ft_print_combn(int n)
{
	int a;
	int tab[10];

	a = 0;
	if (n <= 1 || n >= 10)
		return ;
	while (a <= n - 1)
	{
		a += 48;
		tab[a] = a;
		write(1, &tab[a], 1);
		a -= 48;
		a++;
	}
	write(1, ", ", 2);
}
